#ifndef DTYPES_H
#define DTYPES_H

#include  <inttypes.h>

typedef unsigned char			u8;
typedef unsigned short int		u16;
typedef uint32_t		u32;
typedef unsigned long long int	u64;

typedef signed char				s8;
typedef signed short int		s16;
typedef int32_t		s32;
typedef signed long long int	s64;

typedef float					f32;
typedef double					f64;


#endif
